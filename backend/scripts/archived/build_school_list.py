def main():
    schools_json = []
    for sch_id, name, s_type, province in SCHOOLS:
        schools_json.append({
            "schId": sch_id,
            "name": name,
            "type": s_type,
            "province": province,
        })

    output_path = DATA_DIR / "school_list.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(schools_json, f, ensure_ascii=False, indent=2)

    # 统计
    types = {}
    provinces = {}
    for s in schools_json:
        types[s["type"]] = types.get(s["type"], 0) + 1
        provinces[s["province"]] = provinces.get(s["province"], 0) + 1

    print(f"✅ 共 {len(schools_json)} 所本科高校")
    print(f"\n类型分布:")
    for t, c in sorted(types.items(), key=lambda x: -x[1]):
        print(f"  {t}: {c} 所")
    print(f"\n省份分布 (前10):")
    for p, c in sorted(provinces.items(), key=lambda x: -x[1])[:10]:
        print(f"  {p}: {c} 所")
    print(f"\n已保存: {output_path}")


if __name__ == "__main__":
    main()
